func Narration() {
    rand.Seed(time.Now().UnixNano())

    for _, verse := range verses {
        PrintVerse(verse)
    }
}

func Verse() {
    rand.Seed(time.Now().UnixNano())

    verseIndex := rand.Intn(1000) + 1
    if message, exists := verses[verseIndex]; exists {
        fmt.Println(bcolors.BLUE + `(` + bcolors.Colors() + message + bcolors.BLINK + `` + bcolors.ENDC + bcolors.BLUE + `)` + bcolors.ENDC)
    }
}

func PrintVerse(verse string) {
    for _, char := range verse {
        fmt.Print(string(char))
        time.Sleep(50 * time.Millisecond)
    }
    fmt.Println()
}

